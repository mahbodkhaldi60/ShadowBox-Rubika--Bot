/* ------------------------ */
/* intent detection */
/* ------------------------ */

function detectIntent(text) {

  let best = null
  let bestScore = 0

  for (const intent of intents) {

    const s = score(text, intent.keywords)

    if (s > bestScore) {
      bestScore = s
      best = intent
    }

  }

  return best
}

/* ------------------------ */
/* main reply */
/* ------------------------ */

function getBotReply(message = "") {

  const text = normalize(message)

  if (!text) {
    return `یه چیزی بنویس 😄

${randomChannelMention()}`
  }

  const intent = detectIntent(text)

  let reply

  if (!intent) {

    reply = random([
      "متوجه منظورت نشدم 😅",
      "یه جور دیگه بگو شاید بهتر بفهمم",
      "اگر واضح‌تر بگی بهتر جواب میدم",
      "فکر کنم منظورت رو کامل نگرفتم",
      "دوباره بگو ببینم چی میگی"
    ])

  } else {

    reply = random(intent.responses)

  }

  /* add channel mention */

  return `${reply}

${randomChannelMention()}`
}

