/* ------------------------ */
/* helper functions */
/* ------------------------ */

function normalize(text = "") {
  return text
    .toLowerCase()
    .replace(/[ي]/g, "ی")
    .replace(/[ك]/g, "ک")
    .replace(/\s+/g, " ")
    .trim()
}

function random(arr) {
  return arr[Math.floor(Math.random() * arr.length)];
}

function score(text, keywords) {
  let s = 0
  for (const k of keywords) {
    if (text.includes(k)) s++
  }
  return s
}

